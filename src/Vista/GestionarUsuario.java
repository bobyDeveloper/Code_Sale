    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        try {
            Usuario usuario = new modelo.Usuario();
            Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
            if (idUsuario == 0) {
                JOptionPane.showMessageDialog(null, "¡Seleccione un Usuario!");
            } else {
                if (txt_nombre.getText().isEmpty() || txt_apellidoP.getText().isEmpty() || txt_apellidoM.getText().isEmpty() || txt_usuario.getText().isEmpty()
                        || txt_contraseña.getText().isEmpty() || txt_telefono.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "¡Completa todos los campos!");

                } else {
                    usuario.setNombre(txt_nombre.getText().trim());
                    usuario.setApellidoPaterno(txt_apellidoP.getText().trim());
                    usuario.setApellidoMaterno(txt_apellidoM.getText().trim());
                    usuario.setUsuario(txt_usuario.getText().trim());
                    usuario.setPassword(txt_contraseña.getText().trim());
                    usuario.setTelefono(txt_telefono.getText().trim());
                    usuario.setEstado(1);

                    if (controlUsuario.actualizar(usuario, idUsuario)) {
                        JOptionPane.showMessageDialog(null, "¡Actualizacion Exitosa!");
                        this.CargarTablaUsuarios();
                        this.Limpiar();
                        idUsuario = 0;

                    } else {
                        JOptionPane.showMessageDialog(null, "¡Error al Actualizar usuario!");
                    }
                }
            }
        } catch (SQLException ex) {
            System.out.println("Actualizar Usuario: "+ ex.getMessage());
        }


    }//GEN-LAST:event_btnActualizarActionPerformed

