    private void CargarTablaUsuarios() {
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select * from tb_usuario";
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            GestionarUsuario.jTableUsuarios = new JTable(model);
            GestionarUsuario.jScrollPane1.setViewportView(GestionarUsuario.jTableUsuarios);

            model.addColumn("N°");//ID
            model.addColumn("nombre");
            model.addColumn("apellidoPaterno");
            model.addColumn("apellidoMaterno");
            model.addColumn("usuario");
            model.addColumn("password");
            model.addColumn("telefono");
            model.addColumn("estado");

            while (rs.next()) {
                Object fila[] = new Object[8];
                for (int i = 0; i < 8; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla usuarios: " + e);
        }
        jTableUsuarios.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTableUsuarios.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idUsuario = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosUsuarioSeleccionado(idUsuario);//metodo
                }
            }
        });
    }

    private void EnviarDatosUsuarioSeleccionado(int idUsuario) {
        try {
            PreparedStatement pst = cn.prepareStatement(
                    "select * from tb_usuario where idUsuario = '" + idUsuario + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                txt_nombre.setText(rs.getString("nombre"));
                txt_apellidoP.setText(rs.getString("apellidoPaterno"));
                txt_apellidoM.setText(rs.getString("apellidoMaterno"));
                txt_usuario.setText(rs.getString("usuario"));
                txt_contraseña.setText(rs.getString("password"));
                txt_telefono.setText(rs.getString("telefono"));
            }
        } catch (SQLException e) {
            System.out.println("Error al seleccionar usuario: " + e);
        }
    }

